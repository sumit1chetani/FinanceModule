package com.dci.finance.closingPeriod;

public class ClosingPeriodBean {

	public String fromDate;
	public String toDate;
	public String status;
	public String closedBy;
	public String companyCode;
	public String companyName;

	public String closing_acc_code;
	public String group_head_name;

	public String getFromDate() {
		return fromDate;
	}

	public void setFromDate(String fromDate) {
		this.fromDate = fromDate;
	}

	public String getToDate() {
		return toDate;
	}

	public void setToDate(String toDate) {
		this.toDate = toDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getClosedBy() {
		return closedBy;
	}

	public void setClosedBy(String closedBy) {
		this.closedBy = closedBy;
	}

	public String getCompanyCode() {
		return companyCode;
	}

	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getClosing_acc_code() {
		return closing_acc_code;
	}

	public void setClosing_acc_code(String closing_acc_code) {
		this.closing_acc_code = closing_acc_code;
	}

	public String getGroup_head_name() {
		return group_head_name;
	}

	public void setGroup_head_name(String group_head_name) {
		this.group_head_name = group_head_name;
	}

}
