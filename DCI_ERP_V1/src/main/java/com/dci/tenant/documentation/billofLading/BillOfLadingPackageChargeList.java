package com.dci.tenant.documentation.billofLading;

public class BillOfLadingPackageChargeList {
	private Integer inwardPackageChargeId;
	private String hsCode;
	private String packageType;
	private Double noofPcks;
	private Double gw_;
	private String goods;
	
	public String getHsCode() {
		return hsCode;
	}
	public void setHsCode(String hsCode) {
		this.hsCode = hsCode;
	}
	public String getPackageType() {
		return packageType;
	}
	public void setPackageType(String packageType) {
		this.packageType = packageType;
	}
	public Double getNoofPcks() {
		return noofPcks;
	}
	public void setNoofPcks(Double noofPcks) {
		this.noofPcks = noofPcks;
	}
	public Double getGw_() {
		return gw_;
	}
	public void setGw_(Double gw_) {
		this.gw_ = gw_;
	}
	public String getGoods() {
		return goods;
	}
	public void setGoods(String goods) {
		this.goods = goods;
	}
	public Integer getInwardPackageChargeId() {
		return inwardPackageChargeId;
	}
	public void setInwardPackageChargeId(Integer inwardPackageChargeId) {
		this.inwardPackageChargeId = inwardPackageChargeId;
	}
	 
	
	
	
}
