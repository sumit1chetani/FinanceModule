package com.dci.tenant.finance.reports.financials.invoiceInformation;


public class InvoiceInformation {
	
	private String creditNote;
	private String debitNote;
	private String invoice;
	private String generalInvoice;
	private String voucher;
	private String generalVoucher;
	private String companyCode;
	private String purchaseInvoice;
	private String paymentOrder;
	private String payment;
	private String receipt;
	private double amount;
	
	public String getCreditNote() {
		return creditNote;
	}
	public void setCreditNote(String creditNote) {
		this.creditNote = creditNote;
	}
	public String getDebitNote() {
		return debitNote;
	}
	public void setDebitNote(String debitNote) {
		this.debitNote = debitNote;
	}
	public String getInvoice() {
		return invoice;
	}
	public void setInvoice(String invoice) {
		this.invoice = invoice;
	}
	public String getGeneralInvoice() {
		return generalInvoice;
	}
	public void setGeneralInvoice(String generalInvoice) {
		this.generalInvoice = generalInvoice;
	}
	public String getVoucher() {
		return voucher;
	}
	public void setVoucher(String voucher) {
		this.voucher = voucher;
	}
	public String getGeneralVoucher() {
		return generalVoucher;
	}
	public void setGeneralVoucher(String generalVoucher) {
		this.generalVoucher = generalVoucher;
	}
	public String getCompanyCode() {
		return companyCode;
	}
	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}
	public String getPurchaseInvoice() {
		return purchaseInvoice;
	}
	public void setPurchaseInvoice(String purchaseInvoice) {
		this.purchaseInvoice = purchaseInvoice;
	}
	public String getPaymentOrder() {
		return paymentOrder;
	}
	public void setPaymentOrder(String paymentOrder) {
		this.paymentOrder = paymentOrder;
	}
	public String getPayment() {
		return payment;
	}
	public void setPayment(String payment) {
		this.payment = payment;
	}
	public String getReceipt() {
		return receipt;
	}
	public void setReceipt(String receipt) {
		this.receipt = receipt;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	
}