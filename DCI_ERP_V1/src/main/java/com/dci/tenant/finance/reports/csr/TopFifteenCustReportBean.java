package com.dci.tenant.finance.reports.csr;

public class TopFifteenCustReportBean {

	private String year;

	private String value;

	private String sfpl;

	private String xcl;

	private String oel;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getSsf() {
		return sfpl;
	}

	public void setSsf(String sfpl) {
		this.sfpl = sfpl;
	}

	public String getXcl() {
		return xcl;
	}

	public void setXcl(String xcl) {
		this.xcl = xcl;
	}

	public String getOel() {
		return oel;
	}

	public void setOel(String oel) {
		this.oel = oel;
	}
}
