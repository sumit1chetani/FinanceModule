package com.dci.tenant.finance.reports.csr;

public class TopTenNvoMloReportBean {

	private String year;
	private String nvo;
	private String mlo;
	private String value;
	private String weight;
	private String avg;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getNvo() {
		return nvo;
	}

	public void setNvo(String nvo) {
		this.nvo = nvo;
	}

	public String getMlo() {
		return mlo;
	}

	public void setMlo(String mlo) {
		this.mlo = mlo;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getAvg() {
		return avg;
	}

	public void setAvg(String avg) {
		this.avg = avg;
	}

}
